
//File Details saved in methods

public class FileDetails {

    //File Fields
    public static int difficultylevel;
    public static int minenumber;
    public static int availabletime;
    public static int hypermine;

    //Get Fields
    public int getDifficultylevel() {
        return difficultylevel;
    }

    public int getMineNumber() {
        return minenumber;
    }

    public int getAvailableTime() {
        return availabletime;
    }

    public int getHyperMine() {
        return hypermine;
    }

    //Set Fields
    public void setDifficultylevel(int difl) {
        this.difficultylevel = difl;
    }
    public void setMineNumber(int minenum) {
        this.minenumber = minenum;
    }
    public void setAvailableTime(int availtime) {
        this.availabletime = availtime;
    }
    public void setHyperMine(int hypmine) {
        this.hypermine = hypmine;
    }
}
